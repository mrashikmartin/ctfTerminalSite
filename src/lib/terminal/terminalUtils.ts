import { terminalPathStore } from '$lib/stores/terminalStore';
import { get } from 'svelte/store';
const prefix = 'GDSC-EEC: ~$';
export function attachPrefix(text: string) {
	return getPrefix() + text;
}

export function getPrefix() {
	return prefix.slice(0, -1) + getPath() + '$ ';
}

export function getPrefixStyled() {
	return electricBlueColor(getPrefix());
}
export function getPath() {
	return get(terminalPathStore);
}

export function electricBlueColor(text: string) {
	return `
	<span class="text-electric-blue">${text}</span>`;
}



